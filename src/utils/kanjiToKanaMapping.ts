/**
 * 檢查字符是否為漢字
 */
export const isKanji = (char: string): boolean => {
  return /[\u4e00-\u9faf]/.test(char);
};

/**
 * 獲取漢字的假名讀音
 */
export const getKanaReading = (kanji: string): string | null => {
  return KANJI_TO_KANA_MAP[kanji] || null;
};

/**
 * 檢查字符是否有已知的假名讀音
 */
export const hasKanaReading = (kanji: string): boolean => {
  return kanji in KANJI_TO_KANA_MAP;
}; 